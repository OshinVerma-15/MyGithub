package topic4.operators;

public class BasicOperatorsProgram {

	public static void main(String[] args) {
		
		int num1 = 46;
		int num2 = 22;
		int result;
		
		// Addition of numbers
		result= num1+ num2;
		System.out.println("Addition of numbers:"+ result );
		
        //Subtraction of numbers
		result = num2-num1;
		System.out.println("Subtraction of numbers: "+ result);
		
		// Multiplication
		result= num1*num2;
		System.out.println("Multiplication of numbers: "+ result);
		
		//Division 
		result= num2/num1;
		System.out.println("Division of numbers: "+ result);

		 // Mode
		result = num1% num2;
		System.out.println("Mod of numbers: "+ result);
		
		
}
      }