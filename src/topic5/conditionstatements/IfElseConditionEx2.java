public class IfElseConditionEx2 {

	public static void main(String[] args) {
		
		//Ladder condition
		char a = 'E';
		if (a == 'E')
		{
			System.out.println("If alphabet show as E");
		}
		else if ( a == 'R')
		
		{
			System.out.println("If alphabet doesnot show as E");	
		}
		
		else if (a=='P')
		{
			System.out.println("If alphabet doesnot show as E");
		}
		
		else
		{
			System.out.println("If alphabet doesnot show E");
		}
	}

}
